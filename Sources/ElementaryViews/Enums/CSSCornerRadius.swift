/// Tailwind border-radius utility classes.
public enum CSSCornerRadius: String, Sendable {
    case none = "rounded-none"
    case sm = "rounded-sm"
    case md = "rounded-md"
    case lg = "rounded-lg"
    case xl = "rounded-xl"
    case xl2 = "rounded-2xl"
    case xl3 = "rounded-3xl"
    case full = "rounded-full"
}
