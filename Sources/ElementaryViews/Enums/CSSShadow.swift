/// Tailwind CSS shadow utility classes.
public enum CSSShadow: String, Sendable {
    case none = "shadow-none"
    case sm = "shadow-sm"
    case regular = "shadow"
    case md = "shadow-md"
    case lg = "shadow-lg"
    case xl = "shadow-xl"
    case xl2 = "shadow-2xl"
    case inner = "shadow-inner"
}
